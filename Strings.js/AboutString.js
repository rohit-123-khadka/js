// A String is a data type used to represent text.
// for example: 'hello';


// There are three types or ways to create a String:
// 1. Single Quote = ' '
// for example: 'My name is: Rohit'

// 2. Double Quote = " "
// for example: alert("My name is Rohit")

// 3. back tick = ` `
// for example: alert(`My name is Rohit`)

// There are two types of Escape character:
// 1. \ = it creates double quotation automatically.
// 2. \n = it creates extra new line.